export const supportedBotLanguages = ['ca', 'es', 'en'] as const;

export type BotLanguage = (typeof supportedBotLanguages)[number];

export function isBotLanguage(value: string | undefined): value is BotLanguage {
  return value !== undefined && (supportedBotLanguages as readonly string[]).includes(value);
}

export function normalizeBotLanguage(value: string | undefined, fallback: BotLanguage): BotLanguage {
  return isBotLanguage(value) ? value : fallback;
}

export function languageDisplayName(language: BotLanguage): string {
  if (language === 'ca') return 'Català';
  if (language === 'es') return 'Español';
  return 'English';
}

export function languageNames(): Record<BotLanguage, string> {
  return {
    ca: 'Català',
    es: 'Español',
    en: 'English',
  };
}

